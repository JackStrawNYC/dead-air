/**
 * NeuralWebScene — synaptic network with firing nodes.
 * Uses MultiPassQuad with feedback for signal propagation trails.
 */

import React from "react";
import { AudioReactiveCanvas } from "../components/AudioReactiveCanvas";
import { MultiPassQuad } from "../components/MultiPassQuad";
import { neuralWebVert, neuralWebFrag } from "../shaders/neural-web";
import type { EnhancedFrameData, SectionBoundary, ColorPalette } from "../data/types";

interface Props {
  frames: EnhancedFrameData[];
  sections?: SectionBoundary[];
  palette?: ColorPalette;
  tempo?: number;
  style?: React.CSSProperties;
  jamDensity?: number;
}

export const NeuralWebScene: React.FC<Props> = ({ frames, sections, palette, tempo, style, jamDensity }) => {
  return (
    <AudioReactiveCanvas frames={frames} sections={sections} palette={palette} tempo={tempo} style={style} jamDensity={jamDensity}>
      <MultiPassQuad
        vertexShader={neuralWebVert}
        fragmentShader={neuralWebFrag}
        feedback
      />
    </AudioReactiveCanvas>
  );
};
